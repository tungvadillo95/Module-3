 GO

 -- 2.16 Tên công ty, tên GD, địa chỉ và điện thoại KH và các nhà cung cấp hàng?
 SELECT TENCONGTY 'Tên công ty',
		TENGIAODICH 'Tên giao dịch',
		DIACHI 'Địa chỉ',
		DIENTHOAI 'Điện thoại'
