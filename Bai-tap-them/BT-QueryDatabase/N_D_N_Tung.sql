 WHERE DAY(NGAYSINH) IN (
  SELECT DAY(NGAYSINH) 
  FROM NHANVIEN
  GROUP BY DAY(NGAYSINH)
  HAVING COUNT(DAY(NGAYSINH)) > 1
)
GO

-- 2.15 Những đơn hàng nào yêu cầu giao hàng tại công ty đặt hàng và những đơn đó là của công ty nào
 SELECT D.*,
